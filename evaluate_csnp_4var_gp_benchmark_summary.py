def attr_value(attrs: Dict[str, Any], key: str, default: Any) -> Any:
    v = attrs.get(key, default)
    if isinstance(v, bytes):
        return v.decode("utf-8")
    if isinstance(v, np.generic):
        return v.item()
    return v


def read_h5_meta(h5_path: Path, fallback: Dict[str, Any]) -> Dict[str, Any]:
    meta = dict(fallback)
    with h5py.File(h5_path, "r") as f:
        attrs = dict(f.attrs)
        data_shape = tuple(f["data"].shape)
    meta["num_datasets"] = int(attr_value(attrs, "num_datasets", data_shape[0]))
    meta["num_samples"] = int(attr_value(attrs, "num_samples", data_shape[1]))
    meta["graph_id"] = int(attr_value(attrs, "graph_id", meta.get("graph_id", -1)))
    meta["graph_name"] = str(attr_value(attrs, "graph_name", meta.get("graph_name", "unknown")))
    # For fixed-graph files the directory name is the most useful grouping label
    # (e.g. rff_gaussian). The h5 attrs split that into generator=rff,
    # noise=gaussian, which would merge different noise variants in the summary.
    meta["generator"] = str(meta.get("generator") or attr_value(attrs, "generator", "unknown"))
    return meta


def graph_id_from_folder(path: Path) -> int:
    match = re.match(r"graph_(\d+)__", path.name)
    return int(match.group(1)) if match else -1


def discover_h5_files(args: argparse.Namespace, sample_size: int) -> List[Tuple[Path, Dict[str, Any]]]:
    benchmark_root = Path(args.benchmark_root).expanduser().resolve()
    if args.benchmark_kind == "random_gp":
        pattern = benchmark_root / args.distribution / f"n_{sample_size}" / f"seed_{args.seed}" / "*.h5"
        fallback = {
            "benchmark": "random_gp",
            "generator": args.distribution,
            "graph_id": -1,
            "graph_name": "random_graphs",
        }
        return [(Path(p), read_h5_meta(Path(p), fallback)) for p in sorted(glob.glob(str(pattern)))]

    if args.benchmark_kind == "fixed_graph":
        graph_ids = None if args.graph_ids == "all" else set(parse_int_list(args.graph_ids))
        generators = parse_csv_list(args.fixed_generators)
        out: List[Tuple[Path, Dict[str, Any]]] = []
        for graph_dir in sorted((benchmark_root / str(args.num_nodes)).glob("graph_*")):
            if not graph_dir.is_dir():
                continue
            gid = graph_id_from_folder(graph_dir)
            if graph_ids is not None and gid not in graph_ids:
                continue
            for generator in generators:
                pattern = graph_dir / generator / f"n_{sample_size}" / f"seed_{args.seed}" / "*.h5"
                for p in sorted(glob.glob(str(pattern))):
                    fallback = {
                        "benchmark": "fixed_graph",
                        "generator": generator,
                        "graph_id": gid,
                        "graph_name": graph_dir.name.split("__", 1)[1] if "__" in graph_dir.name else graph_dir.name,
                    }
                    out.append((Path(p), read_h5_meta(Path(p), fallback)))
        return out

    raise ValueError(f"Unsupported benchmark_kind: {args.benchmark_kind}")


def evaluate(args: argparse.Namespace) -> Tuple[pd.DataFrame, pd.DataFrame]:
    if args.device == "auto":
        device = "cuda" if th.cuda.is_available() else "cpu"
    else:
        device = args.device

    csnp_work_dir = Path(args.csnp_work_dir).expanduser().resolve()
    models_root = (
        Path(args.models_root).expanduser().resolve()
        if args.models_root
        else csnp_work_dir / "experiments" / "causal_classification" / "models"
    )
    bak_path = Path(args.bak_model_file).expanduser().resolve()
    sample_sizes = parse_int_list(args.sample_sizes)
    methods = set(parse_csv_list(args.methods))
    prediction_modes = parse_prediction_modes(args.prediction_mode)

    print("=" * 100)
    print("4var CSNP GP benchmark summary evaluation")
    print(f"benchmark_root: {Path(args.benchmark_root).expanduser().resolve()}")
    print(f"models_root:    {models_root}")
    print(f"benchmark_kind: {args.benchmark_kind}")
    print(f"distribution:   {args.distribution}")
    print(f"fixed_generators: {args.fixed_generators}")
    print(f"sample_sizes:   {sample_sizes}")
    print(f"methods:        {sorted(methods)}")
    print(f"prediction_modes: {prediction_modes}")
    print(f"device:         {device}")
    print("=" * 100)

    model_specs: List[Dict[str, Any]] = []
    loaded_models: Dict[str, Tuple[Any, str]] = {}
    if "csnp" in methods:
        model_specs.append(
            {
                "method": args.csnp_method_name,
                "run_name": args.csnp_run_name,
                "checkpoint": args.csnp_checkpoint,
                "source": args.csnp_model_source,
            }
        )
    if "ar" in methods:
        model_specs.append(
            {
                "method": args.ar_method_name,
                "run_name": args.ar_run_name,
                "checkpoint": args.ar_checkpoint,
                "source": args.ar_model_source,
            }
        )
    if args.extra_model_specs_json:
        extra_specs = json.loads(args.extra_model_specs_json)
        if not isinstance(extra_specs, list):
            raise ValueError("--extra_model_specs_json must be a JSON list.")
        for spec in extra_specs:
            missing = {"method", "run_name", "checkpoint", "source"} - set(spec)
            if missing:
                raise ValueError(f"Extra model spec is missing keys: {sorted(missing)}")
            if spec["source"] not in {"bak", "current"}:
                raise ValueError("Extra model spec source must be 'bak' or 'current'.")
            model_specs.append(
                {
                    "method": spec["method"],
                    "run_name": spec["run_name"],
                    "checkpoint": spec["checkpoint"],
                    "source": spec["source"],
                }
            )

    for spec in model_specs:
        print(f"Loading {spec['method']}: {spec['run_name']}/{spec['checkpoint']} source={spec['source']}")
        model, config, model_path = load_csnp_model(
            models_root=models_root,
            run_name=spec["run_name"],
            checkpoint=spec["checkpoint"],
            device=device,
            source=spec["source"],
            bak_path=bak_path,
        )
        base_variant = f"{spec['run_name']}_{spec['checkpoint']}_thr{args.threshold}_perm{args.num_perm_samples}"
        loaded_models[spec["method"]] = (model, base_variant)
        print(f"  loaded: {model_path}")

    group_cols = ["benchmark", "generator", "graph_id", "graph_name", "num_samples", "method", "variant"]
    cpdag_summary = RunningSummary(group_cols=group_cols)
    dag_summary = RunningSummary(group_cols=group_cols)

    start = time.time()
    for n in sample_sizes:
        files = discover_h5_files(args, n)
        if not files:
            if args.skip_missing:
                print(f"[SKIP] no h5 files for n={n}")
                continue
            raise FileNotFoundError(f"No h5 files found for n={n}")

        if args.max_files_per_n is not None:
            files = files[: args.max_files_per_n]

        print("-" * 100)
        print(f"n={n}: files={len(files)}")

        seen_for_n = 0
        for file_idx, (h5_path, meta) in enumerate(files):
            with h5py.File(h5_path, "r") as f:
                data_arr = f["data"]
                label_arr = f["label"]
                file_count = int(data_arr.shape[0])
                limit = file_count if args.max_datasets_per_file is None else min(file_count, args.max_datasets_per_file)

                print(
                    f"  [{file_idx + 1}/{len(files)}] "
                    f"graph={meta['graph_id']} generator={meta['generator']} "
                    f"{h5_path.name} datasets={limit}/{file_count}"
                )
                for data_idx in range(limit):
                    if args.max_datasets_per_n is not None and seen_for_n >= args.max_datasets_per_n:
                        break
                    data = np.asarray(data_arr[data_idx], dtype=np.float32)
                    true_dag = remove_diag(np.asarray(label_arr[data_idx], dtype=int))
                    seen_for_n += 1

                    try:
                        true_dir, true_undir, _ = dag_to_cpdag_by_mec(true_dag)
                    except Exception:
                        for method in methods:
                            group = {
                                "benchmark": meta["benchmark"],
                                "generator": meta["generator"],
                                "graph_id": meta["graph_id"],
                                "graph_name": meta["graph_name"],
                                "num_samples": n,
                                "method": method,
                                "variant": "unknown",
                            }
                            cpdag_summary.add_error(group)
                        continue

                    for method, (model, base_variant) in loaded_models.items():
                        try:
                            prob_graphs, mean_prob = csnp_predict_prob_graphs(
                                data=data,
                                model=model,
                                standardize=args.standardize_csnp,
                                num_perm_samples=args.num_perm_samples,
                                device=device,
                            )
                            for prediction_mode in prediction_modes:
                                variant = f"{base_variant}_{prediction_mode}"
                                group = {
                                    "benchmark": meta["benchmark"],
                                    "generator": meta["generator"],
                                    "graph_id": meta["graph_id"],
                                    "graph_name": meta["graph_name"],
                                    "num_samples": n,
                                    "method": method,
                                    "variant": variant,
                                }
                                cpdag_metrics, dag_metrics = csnp_prediction_metrics(
                                    prob_graphs=prob_graphs,
                                    mean_prob=mean_prob,
                                    true_dag=true_dag,
                                    true_dir=true_dir,
                                    true_undir=true_undir,
                                    threshold=args.threshold,
                                    prediction_mode=prediction_mode,
                                )
                                dag_summary.add_ok(group, dag_metrics)
                                cpdag_summary.add_ok(group, cpdag_metrics)
                        except Exception as e:
                            for prediction_mode in prediction_modes:
                                variant = f"{base_variant}_{prediction_mode}"
                                group = {
                                    "benchmark": meta["benchmark"],
                                    "generator": meta["generator"],
                                    "graph_id": meta["graph_id"],
                                    "graph_name": meta["graph_name"],
                                    "num_samples": n,
                                    "method": method,
                                    "variant": variant,
                                }
                                cpdag_summary.add_error(group)
                                dag_summary.add_error(group)
                            if args.print_errors:
                                print(f"    [ERROR] {method} n={n} idx={data_idx}: {repr(e)}")

                    if "pc" in methods:
                        variant = f"pc_{args.pc_test}_alpha{args.pc_alpha}"
                        group = {
                            "benchmark": meta["benchmark"],
                            "generator": meta["generator"],
                            "graph_id": meta["graph_id"],
                            "graph_name": meta["graph_name"],
                            "num_samples": n,
                            "method": "PC",
                            "variant": variant,
                        }
                        try:
                            pred_dir, pred_undir = run_pc_cpdag(data, args)
                            cpdag_summary.add_ok(group, compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir))
                        except Exception as e:
                            cpdag_summary.add_error(group)
                            if args.print_errors:
                                print(f"    [ERROR] PC n={n} idx={data_idx}: {repr(e)}")

                    if "ges" in methods:
                        variant = f"ges_{args.ges_score_func}"
                        group = {
                            "benchmark": meta["benchmark"],
                            "generator": meta["generator"],
                            "graph_id": meta["graph_id"],
                            "graph_name": meta["graph_name"],
                            "num_samples": n,
                            "method": "GES",
                            "variant": variant,
                        }
                        try:
                            pred_dir, pred_undir = run_ges_cpdag(data, args)
                            cpdag_summary.add_ok(group, compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir))
                        except Exception as e:
                            cpdag_summary.add_error(group)
                            if args.print_errors:
                                print(f"    [ERROR] GES n={n} idx={data_idx}: {repr(e)}")

                    if args.progress_every > 0 and seen_for_n % args.progress_every == 0:
                        print(f"    done n={n}: {seen_for_n}")

                if args.max_datasets_per_n is not None and seen_for_n >= args.max_datasets_per_n:
                    break

    print("=" * 100)
    print(f"Finished. elapsed={time.time() - start:.1f}s")
    return cpdag_summary.to_frame(), dag_summary.to_frame()


