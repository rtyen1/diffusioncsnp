def import_decoder_classes(source: str, bak_path: Path) -> Tuple[Any, Optional[Any]]:
    if source == "current":
        from ml2_meta_causal_discovery.models.causaltransformernp import (
            CausalProbabilisticARDecoder,
            CausalProbabilisticDecoder,
        )

        return CausalProbabilisticDecoder, CausalProbabilisticARDecoder
    if source == "bak":
        loader = SourceFileLoader("csnp_bak_causaltransformernp", str(bak_path))
        spec = importlib.util.spec_from_loader(loader.name, loader)
        if spec is None or spec.loader is None:
            raise ImportError(f"Could not load bak model file: {bak_path}")
        module = importlib.util.module_from_spec(spec)
        spec.loader.exec_module(module)
        return module.CausalProbabilisticDecoder, getattr(module, "CausalProbabilisticARDecoder", None)
    raise ValueError(f"Unknown model source: {source}")


def load_csnp_model(
    *,
    models_root: Path,
    run_name: str,
    checkpoint: str,
    device: str,
    source: str,
    bak_path: Path,
) -> Tuple[Any, Dict[str, Any], Path]:
    model_dir = models_root / run_name
    config_path = model_dir / "config.json"
    model_path = model_dir / checkpoint
    if not config_path.exists():
        raise FileNotFoundError(f"Config not found: {config_path}")
    if not model_path.exists():
        raise FileNotFoundError(f"Model checkpoint not found: {model_path}")

    with open(config_path, "r", encoding="utf-8") as f:
        config = json.load(f)

    CausalProbabilisticDecoder, CausalProbabilisticARDecoder = import_decoder_classes(source, bak_path)
    module = config.get("module", "probabilistic")
    model_kwargs = dict(
        d_model=config["d_model"],
        emb_depth=1,
        dim_feedforward=config["dim_feedforward"],
        nhead=config["nhead"],
        dropout=0.0,
        num_layers_encoder=config["num_layers_encoder"],
        num_layers_decoder=config["num_layers_decoder"],
        num_nodes=config["num_nodes"],
        n_perm_samples=config["n_perm_samples"],
        sinkhorn_iter=config.get("sinkhorn_iter", 300),
        use_positional_encoding=config["use_positional_encoding"],
        device=device,
        dtype=th.float32,
    )
    if module == "probabilistic":
        model = CausalProbabilisticDecoder(**model_kwargs).to(device)
    elif module == "probabilistic_ar":
        if CausalProbabilisticARDecoder is None:
            raise ValueError(f"Model source {source!r} does not contain CausalProbabilisticARDecoder.")
        model = CausalProbabilisticARDecoder(
            **model_kwargs,
            num_topo_order_samples=config.get("num_topo_order_samples", 8),
            ar_hidden_dim=config.get("ar_hidden_dim", None),
        ).to(device)
    else:
        raise ValueError(f"Unsupported module in config: {module!r}")

    try:
        state_dict = th.load(model_path, map_location=device, weights_only=True)
    except TypeError:
        state_dict = th.load(model_path, map_location=device)
    model.load_state_dict(state_dict)
    model.eval()
    return model, config, model_path


def csnp_predict_prob_graphs(
    *,
    data: np.ndarray,
    model: Any,
    standardize: bool,
    num_perm_samples: int,
    device: str,
) -> Tuple[np.ndarray, np.ndarray]:
    x = maybe_standardize(data, standardize=standardize).astype(np.float32)
    inputs = th.tensor(x, dtype=th.float32).unsqueeze(0).to(device)

    old_n_perm_samples = getattr(model, "n_perm_samples", None)
    model.n_perm_samples = int(num_perm_samples)
    with th.no_grad():
        probs = model.forward(inputs, graph=None, is_training=False, mask=None)
    if old_n_perm_samples is not None:
        model.n_perm_samples = old_n_perm_samples

    probs_np = probs.detach().cpu().numpy()
    if probs_np.ndim == 4:
        prob_graphs = probs_np[:, 0]
        mean_prob = prob_graphs.mean(axis=0)
    elif probs_np.ndim == 3:
        prob_graphs = probs_np
        mean_prob = prob_graphs.mean(axis=0)
    elif probs_np.ndim == 2:
        mean_prob = probs_np
        prob_graphs = probs_np[None]
    else:
        raise RuntimeError(f"Unexpected model output shape: {probs_np.shape}")
    prob_graphs = np.asarray(prob_graphs, dtype=np.float32)
    mean_prob = np.asarray(mean_prob, dtype=np.float32)
    np.fill_diagonal(mean_prob, 0.0)
    for k in range(prob_graphs.shape[0]):
        np.fill_diagonal(prob_graphs[k], 0.0)
    return prob_graphs, mean_prob


def csnp_predict_dag(
    *,
    data: np.ndarray,
    model: Any,
    threshold: float,
    standardize: bool,
    num_perm_samples: int,
    device: str,
) -> Tuple[np.ndarray, np.ndarray]:
    prob_graphs, mean_prob = csnp_predict_prob_graphs(
        data=data,
        model=model,
        standardize=standardize,
        num_perm_samples=num_perm_samples,
        device=device,
    )
    del prob_graphs
    return threshold_prob_graph(mean_prob, threshold), mean_prob


def csnp_prediction_metrics(
    *,
    data: Optional[np.ndarray] = None,
    prob_graphs: Optional[np.ndarray] = None,
    mean_prob: Optional[np.ndarray] = None,
    true_dag: np.ndarray,
    true_dir: np.ndarray,
    true_undir: np.ndarray,
    model: Optional[Any] = None,
    threshold: float,
    standardize: bool = True,
    num_perm_samples: int = 200,
    device: str = "cuda",
    prediction_mode: str,
) -> Tuple[Dict[str, float], Dict[str, float]]:
    if prob_graphs is None or mean_prob is None:
        if data is None or model is None:
            raise ValueError("Either pass prob_graphs/mean_prob or pass data/model.")
        prob_graphs, mean_prob = csnp_predict_prob_graphs(
            data=data,
            model=model,
            standardize=standardize,
            num_perm_samples=num_perm_samples,
            device=device,
        )

    if prediction_mode == "mean_threshold":
        pred_dag = threshold_prob_graph(mean_prob, threshold)
        dag_metrics = compute_dag_metrics(true_dag, pred_dag)
        if is_acyclic(pred_dag):
            pred_dir, pred_undir, _ = dag_to_cpdag_by_mec(pred_dag)
            cpdag_valid = 1.0
        else:
            pred_dir = pred_dag
            pred_undir = np.zeros_like(pred_dag)
            cpdag_valid = 0.0
        cpdag_metrics = compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir)
        cpdag_metrics["pred_cpdag_valid"] = cpdag_valid
        return cpdag_metrics, dag_metrics

    sample_dags = [threshold_prob_graph(prob_graph, threshold) for prob_graph in prob_graphs]

    if prediction_mode == "sample_dag_mode":
        dag_counts: Dict[Any, int] = defaultdict(int)
        dag_by_key: Dict[Any, np.ndarray] = {}
        for pred_dag in sample_dags:
            key = dag_key(pred_dag)
            dag_counts[key] += 1
            dag_by_key[key] = pred_dag
        pred_dag = dag_by_key[most_common_item(dag_counts)]
        dag_metrics = compute_dag_metrics(true_dag, pred_dag)
        if is_acyclic(pred_dag):
            pred_dir, pred_undir, _ = dag_to_cpdag_by_mec(pred_dag)
            cpdag_valid = 1.0
        else:
            pred_dir = pred_dag
            pred_undir = np.zeros_like(pred_dag)
            cpdag_valid = 0.0
        cpdag_metrics = compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir)
        cpdag_metrics["pred_cpdag_valid"] = cpdag_valid
        return cpdag_metrics, dag_metrics

    sample_cpdag_metrics: List[Dict[str, float]] = []
    sample_dag_metrics: List[Dict[str, float]] = []
    cpdag_counts: Dict[Any, int] = defaultdict(int)
    cpdag_by_key: Dict[Any, Tuple[np.ndarray, np.ndarray, float]] = {}

    for pred_dag in sample_dags:
        sample_dag_metrics.append(compute_dag_metrics(true_dag, pred_dag))
        if is_acyclic(pred_dag):
            pred_dir, pred_undir, _ = dag_to_cpdag_by_mec(pred_dag)
            cpdag_valid = 1.0
        else:
            pred_dir = pred_dag
            pred_undir = np.zeros_like(pred_dag)
            cpdag_valid = 0.0
        sample_metrics = compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir)
        sample_metrics["pred_cpdag_valid"] = cpdag_valid
        sample_cpdag_metrics.append(sample_metrics)

        key = cpdag_key(pred_dir, pred_undir)
        cpdag_counts[key] += 1
        cpdag_by_key[key] = (pred_dir, pred_undir, cpdag_valid)

    if prediction_mode == "expected_samples":
        return average_metric_dicts(sample_cpdag_metrics), average_metric_dicts(sample_dag_metrics)

    if prediction_mode == "sample_cpdag_mode":
        pred_dir, pred_undir, cpdag_valid = cpdag_by_key[most_common_item(cpdag_counts)]
        cpdag_metrics = compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir)
        cpdag_metrics["pred_cpdag_valid"] = cpdag_valid

        # There may be several DAGs in the modal CPDAG; keep DAG summary as the
        # expected per-sample DAG score rather than inventing a representative DAG.
        dag_metrics = average_metric_dicts(sample_dag_metrics)
        return cpdag_metrics, dag_metrics

    raise ValueError(f"Unsupported prediction_mode: {prediction_mode}")


