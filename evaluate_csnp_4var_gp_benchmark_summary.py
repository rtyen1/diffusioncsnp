def binary_stats(y_true: np.ndarray, y_pred: np.ndarray, prefix: str) -> Dict[str, float]:
    y_true = np.asarray(y_true).astype(int).reshape(-1)
    y_pred = np.asarray(y_pred).astype(int).reshape(-1)
    tp = int(((y_true == 1) & (y_pred == 1)).sum())
    fp = int(((y_true == 0) & (y_pred == 1)).sum())
    fn = int(((y_true == 1) & (y_pred == 0)).sum())
    tn = int(((y_true == 0) & (y_pred == 0)).sum())
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
    acc = (tp + tn) / (tp + fp + fn + tn) if (tp + fp + fn + tn) > 0 else np.nan
    return {
        f"{prefix}_tp": float(tp),
        f"{prefix}_fp": float(fp),
        f"{prefix}_fn": float(fn),
        f"{prefix}_tn": float(tn),
        f"{prefix}_precision": float(precision),
        f"{prefix}_recall": float(recall),
        f"{prefix}_f1": float(f1),
        f"{prefix}_accuracy": float(acc),
    }


def cpdag_pair_state(directed: np.ndarray, undirected: np.ndarray, i: int, j: int) -> str:
    directed = remove_diag(directed).astype(int)
    undirected = remove_diag(undirected).astype(int)
    has_undir = undirected[i, j] == 1 or undirected[j, i] == 1
    has_ij = directed[i, j] == 1
    has_ji = directed[j, i] == 1
    if has_undir and (has_ij or has_ji):
        return "invalid_mixed"
    if has_ij and has_ji:
        return "invalid_bidirected"
    if has_undir:
        return "undirected"
    if has_ij:
        return f"{i}->{j}"
    if has_ji:
        return f"{j}->{i}"
    return "none"


def cpdag_shd_pairwise(true_dir: np.ndarray, true_undir: np.ndarray, pred_dir: np.ndarray, pred_undir: np.ndarray) -> int:
    d = true_dir.shape[0]
    shd = 0
    for i in range(d):
        for j in range(i + 1, d):
            if cpdag_pair_state(true_dir, true_undir, i, j) != cpdag_pair_state(pred_dir, pred_undir, i, j):
                shd += 1
    return int(shd)


def dag_shd_pairwise(true_dag: np.ndarray, pred_dag: np.ndarray) -> int:
    true_dag = remove_diag(true_dag).astype(int)
    pred_dag = remove_diag(pred_dag).astype(int)
    d = true_dag.shape[0]
    shd = 0
    for i in range(d):
        for j in range(i + 1, d):
            true_state = "none"
            pred_state = "none"
            if true_dag[i, j] == 1:
                true_state = f"{i}->{j}"
            elif true_dag[j, i] == 1:
                true_state = f"{j}->{i}"
            if pred_dag[i, j] == 1 and pred_dag[j, i] == 1:
                pred_state = "bidirected"
            elif pred_dag[i, j] == 1:
                pred_state = f"{i}->{j}"
            elif pred_dag[j, i] == 1:
                pred_state = f"{j}->{i}"
            if true_state != pred_state:
                shd += 1
    return int(shd)


def compute_cpdag_metrics(true_dir: np.ndarray, true_undir: np.ndarray, pred_dir: np.ndarray, pred_undir: np.ndarray) -> Dict[str, float]:
    true_dir = remove_diag(true_dir).astype(int)
    true_undir = remove_diag(true_undir).astype(int)
    pred_dir = remove_diag(pred_dir).astype(int)
    pred_undir = remove_diag(pred_undir).astype(int)
    d = true_dir.shape[0]

    true_skel = skeleton_from_cpdag(true_dir, true_undir)
    pred_skel = skeleton_from_cpdag(pred_dir, pred_undir)
    pairs = pair_order(d)
    edges = edge_order(d)

    row: Dict[str, float] = {
        "cpdag_exact_match": float(np.array_equal(true_dir, pred_dir) and np.array_equal(true_undir, pred_undir)),
        "cpdag_shd_pairwise": float(cpdag_shd_pairwise(true_dir, true_undir, pred_dir, pred_undir)),
        "skeleton_exact_match": float(np.array_equal(true_skel, pred_skel)),
        "directed_exact_match": float(np.array_equal(true_dir, pred_dir)),
        "undirected_exact_match": float(np.array_equal(true_undir, pred_undir)),
    }
    row.update(binary_stats(np.array([true_skel[i, j] for i, j in pairs]), np.array([pred_skel[i, j] for i, j in pairs]), "skeleton"))
    row.update(binary_stats(np.array([true_dir[i, j] for i, j in edges]), np.array([pred_dir[i, j] for i, j in edges]), "directed"))
    row.update(
        binary_stats(
            np.array([1 if true_undir[i, j] == 1 or true_undir[j, i] == 1 else 0 for i, j in pairs]),
            np.array([1 if pred_undir[i, j] == 1 or pred_undir[j, i] == 1 else 0 for i, j in pairs]),
            "undirected",
        )
    )
    return row


def compute_dag_metrics(true_dag: np.ndarray, pred_dag: np.ndarray) -> Dict[str, float]:
    true_dag = remove_diag(true_dag).astype(int)
    pred_dag = remove_diag(pred_dag).astype(int)
    d = true_dag.shape[0]
    edges = edge_order(d)
    pairs = pair_order(d)
    true_skel = skeleton_from_dag_or_directed(true_dag)
    pred_skel = skeleton_from_dag_or_directed(pred_dag)

    row: Dict[str, float] = {
        "dag_exact_match": float(np.array_equal(true_dag, pred_dag)),
        "dag_shd_pairwise": float(dag_shd_pairwise(true_dag, pred_dag)),
        "pred_is_dag": float(is_acyclic(pred_dag)),
        "skeleton_exact_match": float(np.array_equal(true_skel, pred_skel)),
    }
    row.update(binary_stats(np.array([true_dag[i, j] for i, j in edges]), np.array([pred_dag[i, j] for i, j in edges]), "directed_edge"))
    row.update(binary_stats(np.array([true_skel[i, j] for i, j in pairs]), np.array([pred_skel[i, j] for i, j in pairs]), "skeleton"))
    return row


def average_metric_dicts(metric_dicts: Sequence[Dict[str, float]]) -> Dict[str, float]:
    if not metric_dicts:
        return {}
    keys = sorted({k for row in metric_dicts for k in row})
    out: Dict[str, float] = {}
    for key in keys:
        vals = [row[key] for row in metric_dicts if key in row and not pd.isna(row[key])]
        out[key] = float(np.mean(vals)) if vals else np.nan
    return out


def threshold_prob_graph(prob_graph: np.ndarray, threshold: float) -> np.ndarray:
    pred = (np.asarray(prob_graph) > threshold).astype(int)
    np.fill_diagonal(pred, 0)
    return pred


def matrix_key(mat: np.ndarray) -> Tuple[int, ...]:
    return tuple(np.asarray(mat, dtype=int).reshape(-1).tolist())


def dag_key(dag: np.ndarray) -> Tuple[int, ...]:
    return matrix_key(remove_diag(dag))


def cpdag_key(directed: np.ndarray, undirected: np.ndarray) -> Tuple[Tuple[int, ...], Tuple[int, ...]]:
    return matrix_key(remove_diag(directed)), matrix_key(remove_diag(undirected))


def most_common_item(counter: Dict[Any, int]) -> Any:
    # Deterministic tie break: count desc, then key lexicographic.
    return sorted(counter.items(), key=lambda kv: (-kv[1], str(kv[0])))[0][0]


