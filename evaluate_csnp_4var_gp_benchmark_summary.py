def causallearn_cpdag_to_arrays(graph_obj: Any) -> Tuple[np.ndarray, np.ndarray]:
    mat = np.asarray(graph_obj.graph).copy()
    d = mat.shape[0]
    directed = np.zeros((d, d), dtype=np.int32)
    undirected = np.zeros((d, d), dtype=np.int32)
    for i in range(d):
        for j in range(d):
            if i == j:
                continue
            if mat[i, j] == -1 and mat[j, i] == 1:
                directed[i, j] = 1
            elif mat[i, j] == -1 and mat[j, i] == -1:
                undirected[i, j] = 1
    np.fill_diagonal(directed, 0)
    np.fill_diagonal(undirected, 0)
    return directed, undirected


def run_pc_cpdag(data: np.ndarray, args: argparse.Namespace) -> Tuple[np.ndarray, np.ndarray]:
    from causallearn.search.ConstraintBased.PC import pc
    from causallearn.utils.cit import chisq, fisherz, gsq, kci

    tests = {
        "fisherz": fisherz,
        "kci": kci,
        "chisq": chisq,
        "gsq": gsq,
    }
    if args.pc_test not in tests:
        raise ValueError(f"Unsupported pc_test: {args.pc_test}")
    x = maybe_standardize(data, standardize=args.standardize_traditional)
    cg = pc(
        x,
        alpha=args.pc_alpha,
        indep_test=tests[args.pc_test],
        stable=args.pc_stable,
        uc_rule=args.pc_uc_rule,
        uc_priority=args.pc_uc_priority,
        show_progress=False,
    )
    return causallearn_cpdag_to_arrays(cg.G)


def run_ges_cpdag(data: np.ndarray, args: argparse.Namespace) -> Tuple[np.ndarray, np.ndarray]:
    from causallearn.search.ScoreBased.GES import ges

    x = maybe_standardize(data, standardize=args.standardize_traditional)
    result = ges(x, score_func=args.ges_score_func)
    return causallearn_cpdag_to_arrays(result["G"])


