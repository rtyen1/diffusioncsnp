def parse_int_list(s: str) -> List[int]:
    return [int(x.strip()) for x in str(s).split(",") if x.strip()]


def remove_diag(g: np.ndarray) -> np.ndarray:
    out = np.asarray(g).copy()
    np.fill_diagonal(out, 0)
    return out


def maybe_standardize(x: np.ndarray, standardize: bool = True) -> np.ndarray:
    x = np.asarray(x, dtype=np.float64)
    if not standardize:
        return x
    return (x - x.mean(axis=0, keepdims=True)) / (x.std(axis=0, keepdims=True) + 1e-8)


def is_acyclic(adj: np.ndarray) -> bool:
    adj = remove_diag(adj).astype(int)
    d = adj.shape[0]
    indeg = adj.sum(axis=0).astype(int)
    queue = [i for i in range(d) if indeg[i] == 0]
    visited = 0
    while queue:
        u = queue.pop(0)
        visited += 1
        for v in np.where(adj[u] == 1)[0]:
            indeg[v] -= 1
            if indeg[v] == 0:
                queue.append(int(v))
    return visited == d


def skeleton_from_dag_or_directed(g: np.ndarray) -> np.ndarray:
    g = remove_diag(g).astype(int)
    skel = ((g + g.T) > 0).astype(int)
    np.fill_diagonal(skel, 0)
    return skel


def skeleton_from_cpdag(directed: np.ndarray, undirected: np.ndarray) -> np.ndarray:
    directed = remove_diag(directed).astype(int)
    undirected = remove_diag(undirected).astype(int)
    skel = ((directed + directed.T + undirected + undirected.T) > 0).astype(int)
    np.fill_diagonal(skel, 0)
    return skel


def matrix_to_directed_edges(g: np.ndarray) -> str:
    g = remove_diag(g).astype(int)
    d = g.shape[0]
    edges: List[str] = []
    for i in range(d):
        for j in range(d):
            if i != j and g[i, j] == 1:
                edges.append(f"{i}->{j}")
    return ", ".join(edges) if edges else "(none)"


def matrix_to_undirected_edges(u: np.ndarray) -> str:
    u = remove_diag(u).astype(int)
    d = u.shape[0]
    edges: List[str] = []
    for i in range(d):
        for j in range(i + 1, d):
            if u[i, j] == 1 or u[j, i] == 1:
                edges.append(f"{i}-{j}")
    return ", ".join(edges) if edges else "(none)"


def cpdag_to_string(directed: np.ndarray, undirected: np.ndarray) -> str:
    return (
        f"directed: {matrix_to_directed_edges(directed)}; "
        f"undirected: {matrix_to_undirected_edges(undirected)}"
    )


def flat_directed_columns(prefix: str, g: np.ndarray) -> Dict[str, Any]:
    g = remove_diag(g)
    return {f"{prefix}_{i}to{j}": float(g[i, j]) for i, j in EDGE_ORDER_3VAR}


def flat_undirected_columns(prefix: str, u: np.ndarray) -> Dict[str, Any]:
    u = remove_diag(u)
    return {
        f"{prefix}_{i}_{j}": float(1 if (u[i, j] == 1 or u[j, i] == 1) else 0)
        for i, j in PAIR_ORDER_3VAR
    }


# =============================================================================
# DAG -> CPDAG conversion, same logic as previous CPDAG benchmark script
# =============================================================================

def unshielded_colliders(dag: np.ndarray) -> Set[Tuple[int, int, int]]:
    """Return all unshielded colliders i -> k <- j, with i < j in tuple."""
    dag = remove_diag(dag).astype(int)
    d = dag.shape[0]
    colliders: Set[Tuple[int, int, int]] = set()
    for k in range(d):
        parents = [i for i in range(d) if dag[i, k] == 1]
        for a in range(len(parents)):
            for b in range(a + 1, len(parents)):
                i, j = parents[a], parents[b]
                if dag[i, j] == 0 and dag[j, i] == 0:
                    lo, hi = sorted((i, j))
                    colliders.add((lo, k, hi))
    return colliders


def enumerate_dags_with_skeleton(skel: np.ndarray) -> List[np.ndarray]:
    """Enumerate all acyclic orientations of a tiny skeleton."""
    skel = remove_diag(skel).astype(int)
    d = skel.shape[0]
    pairs = [(i, j) for i in range(d) for j in range(i + 1, d) if skel[i, j] == 1 or skel[j, i] == 1]
    dags: List[np.ndarray] = []
    for mask in range(1 << len(pairs)):
        adj = np.zeros((d, d), dtype=int)
        for bit_idx, (i, j) in enumerate(pairs):
            if (mask >> bit_idx) & 1:
                adj[i, j] = 1
            else:
                adj[j, i] = 1
        if is_acyclic(adj):
            dags.append(adj)
    return dags


def dag_to_cpdag_by_mec(dag: np.ndarray) -> Tuple[np.ndarray, np.ndarray, int]:
    """
    Convert a small DAG to CPDAG by enumerating its Markov equivalence class.

    Two DAGs are Markov equivalent iff they have the same skeleton and the same
    unshielded colliders. For 3 variables this enumeration is tiny and robust.

    Returns:
      directed, undirected, mec_size
    """
    dag = remove_diag(dag).astype(int)
    if not is_acyclic(dag):
        raise ValueError("Input graph is not a DAG; CPDAG is undefined.")

    skel = skeleton_from_dag_or_directed(dag)
    target_colliders = unshielded_colliders(dag)
    candidates = [cand for cand in enumerate_dags_with_skeleton(skel) if unshielded_colliders(cand) == target_colliders]
    if not candidates:
        raise RuntimeError("No Markov equivalent DAGs found. This should not happen.")

    d = dag.shape[0]
    directed = np.zeros((d, d), dtype=int)
    undirected = np.zeros((d, d), dtype=int)

    for i in range(d):
        for j in range(i + 1, d):
            if skel[i, j] == 0 and skel[j, i] == 0:
                continue
            orientations: List[Tuple[int, int]] = []
            for cand in candidates:
                if cand[i, j] == 1:
                    orientations.append((i, j))
                elif cand[j, i] == 1:
                    orientations.append((j, i))
                else:
                    raise RuntimeError("Candidate DAG does not match skeleton.")
            unique_orientations = set(orientations)
            if len(unique_orientations) == 1:
                a, b = next(iter(unique_orientations))
                directed[a, b] = 1
            else:
                undirected[i, j] = 1
                undirected[j, i] = 1

    np.fill_diagonal(directed, 0)
    np.fill_diagonal(undirected, 0)
    return directed, undirected, len(candidates)


