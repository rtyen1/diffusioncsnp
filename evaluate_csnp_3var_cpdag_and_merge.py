def build_ok_row(
    *,
    graph_id: int,
    graph_name: str,
    graph_bitstring: str,
    num_samples: int,
    data_idx: int,
    h5_path: Path,
    method: str,
    variant: str,
    true_dag: np.ndarray,
    true_dir: np.ndarray,
    true_undir: np.ndarray,
    true_mec_size: int,
    pred_dir: np.ndarray,
    pred_undir: np.ndarray,
    extra: Optional[Dict[str, Any]] = None,
) -> Dict[str, Any]:
    row: Dict[str, Any] = {
        "graph_id": graph_id,
        "graph_name": graph_name,
        "graph_bitstring": graph_bitstring,
        "num_samples": num_samples,
        "data_idx": data_idx,
        "h5_path": str(h5_path),
        "method": method,
        "variant": variant,
        "status": "ok",
        "true_dag_edges": matrix_to_directed_edges(true_dag),
        "true_cpdag_directed_edges": matrix_to_directed_edges(true_dir),
        "true_cpdag_undirected_edges": matrix_to_undirected_edges(true_undir),
        "pred_cpdag_directed_edges": matrix_to_directed_edges(pred_dir),
        "pred_cpdag_undirected_edges": matrix_to_undirected_edges(pred_undir),
        "true_cpdag": cpdag_to_string(true_dir, true_undir),
        "pred_cpdag": cpdag_to_string(pred_dir, pred_undir),
        "true_mec_size": true_mec_size,
    }
    if extra:
        row.update(extra)
    row.update(compute_cpdag_metrics(true_dir, true_undir, pred_dir, pred_undir))
    row.update(flat_directed_columns("true_dag", true_dag))
    row.update(flat_directed_columns("true_cpdag_dir", true_dir))
    row.update(flat_undirected_columns("true_cpdag_undir", true_undir))
    row.update(flat_directed_columns("pred_cpdag_dir", pred_dir))
    row.update(flat_undirected_columns("pred_cpdag_undir", pred_undir))
    return row


