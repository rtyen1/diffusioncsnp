def main() -> None:
    args = parse_args()
    results_dir = Path(args.results_dir).expanduser().resolve()
    results_dir.mkdir(parents=True, exist_ok=True)

    csnp_raw_path = results_dir / args.csnp_raw_name
    existing_raw_path = results_dir / args.existing_raw_name
    merged_raw_path = results_dir / args.merged_raw_name
    merged_summary_path = results_dir / args.merged_summary_name

    csnp_df = evaluate_csnp(args)
    csnp_df.to_csv(csnp_raw_path, index=False)
    print(f"Wrote CSNP raw results: {csnp_raw_path}")

    if args.no_merge:
        print("--no_merge was set; stopping after CSNP raw output.")
        return

    if existing_raw_path.exists():
        old_df = pd.read_csv(existing_raw_path)
        print(f"Loaded existing raw results: {existing_raw_path}, rows={len(old_df)}")
    else:
        old_df = pd.DataFrame()
        print(f"Existing raw results not found: {existing_raw_path}. Merged file will contain only CSNP rows.")

    # Avoid duplicating this exact CSNP run if user reruns merge, while keeping
    # other CSNP variants such as older bak/current comparisons.
    if (
        not old_df.empty
        and "method" in old_df.columns
        and "variant" in old_df.columns
        and "method" in csnp_df.columns
        and "variant" in csnp_df.columns
    ):
        current_keys = set(zip(csnp_df["method"], csnp_df["variant"]))
        keep_old = ~old_df[["method", "variant"]].apply(tuple, axis=1).isin(current_keys)
        old_without_current_run = old_df[keep_old].copy()
    else:
        old_without_current_run = old_df

    merged_df = concat_preserve_columns([old_without_current_run, csnp_df])
    merged_df.to_csv(merged_raw_path, index=False)
    print(f"Wrote merged raw results: {merged_raw_path}, rows={len(merged_df)}")

    summary_df = make_summary(merged_df)
    summary_df.to_csv(merged_summary_path, index=False)
    print(f"Wrote merged summary results: {merged_summary_path}, rows={len(summary_df)}")

    if args.replace_original:
        raw_original = results_dir / "raw_results_cpdag.csv"
        summary_original = results_dir / "summary_results_cpdag.csv"
        merged_df.to_csv(raw_original, index=False)
        summary_df.to_csv(summary_original, index=False)
        print("Replaced original result CSVs:")
        print(f"  {raw_original}")
        print(f"  {summary_original}")

    print("Done.")


if __name__ == "__main__":
    main()
