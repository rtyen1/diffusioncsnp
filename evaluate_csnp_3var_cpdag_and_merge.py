def evaluate_csnp(args: argparse.Namespace) -> pd.DataFrame:
    csnp_work_dir = Path(args.csnp_work_dir).expanduser().resolve()
    benchmark_root = Path(args.benchmark_root).expanduser().resolve()

    if args.device == "auto":
        device = "cuda" if th.cuda.is_available() else "cpu"
    else:
        device = args.device

    print("=" * 100)
    print("Loading CSNP model")
    print("=" * 100)
    print(f"csnp_work_dir: {csnp_work_dir}")
    print(f"run_name:      {args.run_name}")
    print(f"checkpoint:    {args.model_checkpoint}")
    print(f"device:        {device}")

    model, config, model_path = load_csnp_model(
        csnp_work_dir=csnp_work_dir,
        run_name=args.run_name,
        model_checkpoint=args.model_checkpoint,
        device=device,
    )
    print(f"Loaded model: {model_path}")

    graph_ids = parse_int_list(args.graph_ids)
    sample_sizes = parse_int_list(args.sample_sizes)
    variant = f"{args.run_name}_{args.model_checkpoint}_meanprob_thr{args.threshold}_perm{args.num_perm_samples}"

    rows: List[Dict[str, Any]] = []
    start_time = time.time()

    for graph_id in graph_ids:
        for n in sample_sizes:
            try:
                h5_path = find_h5_path(
                    benchmark_root=benchmark_root,
                    graph_id=graph_id,
                    num_samples=n,
                    seed=args.seed,
                    num_datasets=args.num_datasets,
                    mechanism=args.mechanism,
                    noise=args.noise,
                )
            except FileNotFoundError as e:
                if args.skip_missing:
                    print(f"[SKIP] {e}")
                    continue
                raise

            meta = read_h5_metadata(h5_path)
            graph_name = meta["graph_name"]
            graph_bitstring = meta["graph_bitstring"]
            n_in_file = int(meta["num_datasets"])
            limit = n_in_file if args.max_datasets_per_file is None else min(n_in_file, args.max_datasets_per_file)

            print("-" * 100)
            print(f"Evaluating graph_id={graph_id}, graph_name={graph_name}, n={n}, datasets={limit}/{n_in_file}")
            print(f"h5: {h5_path}")

            with h5py.File(h5_path, "r") as f:
                for data_idx in range(limit):
                    data = np.asarray(f["data"][data_idx], dtype=np.float32)
                    true_dag = remove_diag(np.asarray(f["label"][data_idx], dtype=int))

                    try:
                        true_dir, true_undir, true_mec_size = dag_to_cpdag_by_mec(true_dag)
                        pred_dir, pred_undir, extra = run_csnp_cpdag(
                            data=data,
                            model=model,
                            threshold=args.threshold,
                            standardize=args.standardize_csnp,
                            num_perm_samples=args.num_perm_samples,
                            device=device,
                        )
                        extra["model_path"] = str(model_path)
                        rows.append(
                            build_ok_row(
                                graph_id=graph_id,
                                graph_name=graph_name,
                                graph_bitstring=graph_bitstring,
                                num_samples=n,
                                data_idx=data_idx,
                                h5_path=h5_path,
                                method="CSNP",
                                variant=variant,
                                true_dag=true_dag,
                                true_dir=true_dir,
                                true_undir=true_undir,
                                true_mec_size=true_mec_size,
                                pred_dir=pred_dir,
                                pred_undir=pred_undir,
                                extra=extra,
                            )
                        )
                    except Exception as e:
                        rows.append(
                            build_error_row(
                                graph_id=graph_id,
                                graph_name=graph_name,
                                graph_bitstring=graph_bitstring,
                                num_samples=n,
                                data_idx=data_idx,
                                h5_path=h5_path,
                                method="CSNP",
                                variant=variant,
                                error=e,
                            )
                        )

                    if args.progress_every > 0 and (data_idx + 1) % args.progress_every == 0:
                        print(f"  done {data_idx + 1}/{limit}")

    elapsed = time.time() - start_time
    print("=" * 100)
    print(f"Finished CSNP evaluation. rows={len(rows)}, elapsed={elapsed:.1f}s")
    return pd.DataFrame(rows)


