def binary_stats(y_true: np.ndarray, y_pred: np.ndarray, prefix: str) -> Dict[str, Any]:
    y_true = np.asarray(y_true).astype(int).reshape(-1)
    y_pred = np.asarray(y_pred).astype(int).reshape(-1)
    tp = int(((y_true == 1) & (y_pred == 1)).sum())
    fp = int(((y_true == 0) & (y_pred == 1)).sum())
    fn = int(((y_true == 1) & (y_pred == 0)).sum())
    tn = int(((y_true == 0) & (y_pred == 0)).sum())
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
    acc = (tp + tn) / (tp + fp + fn + tn) if (tp + fp + fn + tn) > 0 else np.nan
    return {
        f"{prefix}_tp": tp,
        f"{prefix}_fp": fp,
        f"{prefix}_fn": fn,
        f"{prefix}_tn": tn,
        f"{prefix}_precision": precision,
        f"{prefix}_recall": recall,
        f"{prefix}_f1": f1,
        f"{prefix}_accuracy": acc,
    }


def cpdag_pair_state(directed: np.ndarray, undirected: np.ndarray, i: int, j: int) -> str:
    directed = remove_diag(directed).astype(int)
    undirected = remove_diag(undirected).astype(int)
    has_undir = undirected[i, j] == 1 or undirected[j, i] == 1
    has_ij = directed[i, j] == 1
    has_ji = directed[j, i] == 1
    if has_undir and (has_ij or has_ji):
        return "invalid_mixed"
    if has_ij and has_ji:
        return "invalid_bidirected"
    if has_undir:
        return "undirected"
    if has_ij:
        return f"{i}->{j}"
    if has_ji:
        return f"{j}->{i}"
    return "none"


def cpdag_shd_pairwise(true_dir: np.ndarray, true_undir: np.ndarray, pred_dir: np.ndarray, pred_undir: np.ndarray) -> int:
    shd = 0
    d = true_dir.shape[0]
    for i in range(d):
        for j in range(i + 1, d):
            if cpdag_pair_state(true_dir, true_undir, i, j) != cpdag_pair_state(pred_dir, pred_undir, i, j):
                shd += 1
    return int(shd)


def compute_cpdag_metrics(true_dir: np.ndarray, true_undir: np.ndarray, pred_dir: np.ndarray, pred_undir: np.ndarray) -> Dict[str, Any]:
    true_dir = remove_diag(true_dir).astype(int)
    true_undir = remove_diag(true_undir).astype(int)
    pred_dir = remove_diag(pred_dir).astype(int)
    pred_undir = remove_diag(pred_undir).astype(int)

    row: Dict[str, Any] = {}
    row["cpdag_exact_match"] = bool(np.array_equal(true_dir, pred_dir) and np.array_equal(true_undir, pred_undir))
    row["cpdag_shd_pairwise"] = cpdag_shd_pairwise(true_dir, true_undir, pred_dir, pred_undir)

    true_skel = skeleton_from_cpdag(true_dir, true_undir)
    pred_skel = skeleton_from_cpdag(pred_dir, pred_undir)
    skel_true_vec = np.array([true_skel[i, j] for i, j in PAIR_ORDER_3VAR], dtype=int)
    skel_pred_vec = np.array([pred_skel[i, j] for i, j in PAIR_ORDER_3VAR], dtype=int)
    row["skeleton_exact_match"] = bool(np.array_equal(skel_true_vec, skel_pred_vec))
    row.update(binary_stats(skel_true_vec, skel_pred_vec, prefix="skeleton"))

    dir_true_vec = np.array([true_dir[i, j] for i, j in EDGE_ORDER_3VAR], dtype=int)
    dir_pred_vec = np.array([pred_dir[i, j] for i, j in EDGE_ORDER_3VAR], dtype=int)
    row["directed_exact_match"] = bool(np.array_equal(dir_true_vec, dir_pred_vec))
    row.update(binary_stats(dir_true_vec, dir_pred_vec, prefix="directed"))

    undir_true_vec = np.array([1 if (true_undir[i, j] == 1 or true_undir[j, i] == 1) else 0 for i, j in PAIR_ORDER_3VAR], dtype=int)
    undir_pred_vec = np.array([1 if (pred_undir[i, j] == 1 or pred_undir[j, i] == 1) else 0 for i, j in PAIR_ORDER_3VAR], dtype=int)
    row["undirected_exact_match"] = bool(np.array_equal(undir_true_vec, undir_pred_vec))
    row.update(binary_stats(undir_true_vec, undir_pred_vec, prefix="undirected"))
    return row


