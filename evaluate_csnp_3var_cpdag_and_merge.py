def load_csnp_model(csnp_work_dir: Path, run_name: str, model_checkpoint: str, device: str) -> Tuple[Any, Dict[str, Any], Path]:
    model_dir = csnp_work_dir / "experiments" / "causal_classification" / "models" / run_name
    config_path = model_dir / "config.json"
    model_path = model_dir / model_checkpoint

    if not config_path.exists():
        raise FileNotFoundError(f"Config not found: {config_path}")
    if not model_path.exists():
        raise FileNotFoundError(f"Model checkpoint not found: {model_path}")

    with open(config_path, "r", encoding="utf-8") as f:
        config = json.load(f)

    module = config.get("module", "probabilistic")
    model_kwargs = dict(
        d_model=config["d_model"],
        emb_depth=1,
        dim_feedforward=config["dim_feedforward"],
        nhead=config["nhead"],
        dropout=0.0,
        num_layers_encoder=config["num_layers_encoder"],
        num_layers_decoder=config["num_layers_decoder"],
        num_nodes=config["num_nodes"],
        n_perm_samples=config["n_perm_samples"],
        sinkhorn_iter=config["sinkhorn_iter"],
        use_positional_encoding=config["use_positional_encoding"],
        device=device,
        dtype=th.float32,
    )
    if module == "probabilistic":
        model = CausalProbabilisticDecoder(**model_kwargs).to(device)
    elif module == "probabilistic_ar":
        model = CausalProbabilisticARDecoder(
            **model_kwargs,
            num_topo_order_samples=config.get("num_topo_order_samples", 8),
            ar_hidden_dim=config.get("ar_hidden_dim", None),
        ).to(device)
    else:
        raise ValueError(
            f"Unsupported CSNP module in config: {module!r}. "
            "This evaluator currently supports 'probabilistic' and 'probabilistic_ar'."
        )

    try:
        state_dict = th.load(model_path, map_location=device, weights_only=True)
    except TypeError:
        # For older PyTorch versions without weights_only.
        state_dict = th.load(model_path, map_location=device)

    model.load_state_dict(state_dict)
    model.eval()
    return model, config, model_path


def run_csnp_cpdag(
    data: np.ndarray,
    model: Any,
    threshold: float,
    standardize: bool,
    num_perm_samples: int,
    device: str,
) -> Tuple[np.ndarray, np.ndarray, Dict[str, Any]]:
    """
    CSNP output -> mean probability matrix -> threshold DAG -> CPDAG.

    If thresholded output is cyclic/bidirected, strict CPDAG is undefined. We keep
    the thresholded directed graph as a proxy, set pred_cpdag_valid=False, and its
    exact match will normally be False.
    """
    x = maybe_standardize(data, standardize=standardize).astype(np.float32)
    inputs = th.tensor(x, dtype=th.float32).unsqueeze(0).to(device)

    old_n_perm_samples = getattr(model, "n_perm_samples", None)
    model.n_perm_samples = int(num_perm_samples)

    with th.no_grad():
        probs = model.forward(inputs, graph=None, is_training=False, mask=None)

    if old_n_perm_samples is not None:
        model.n_perm_samples = old_n_perm_samples

    probs_np = probs.detach().cpu().numpy()
    # Expected shape: [num_perm_samples, batch, d, d]
    if probs_np.ndim == 4:
        prob_graphs = probs_np[:, 0]
        mean_prob = prob_graphs.mean(axis=0)
    elif probs_np.ndim == 3:
        mean_prob = probs_np[0]
    elif probs_np.ndim == 2:
        mean_prob = probs_np
    else:
        raise RuntimeError(f"Unexpected CSNP probability output shape: {probs_np.shape}")

    mean_prob = np.asarray(mean_prob, dtype=np.float32)
    np.fill_diagonal(mean_prob, 0.0)

    pred_dag = (mean_prob > threshold).astype(int)
    np.fill_diagonal(pred_dag, 0)

    pred_is_dag = bool(is_acyclic(pred_dag))
    pred_cpdag_valid = False
    mec_size = np.nan
    if pred_is_dag:
        pred_dir, pred_undir, mec_size = dag_to_cpdag_by_mec(pred_dag)
        pred_cpdag_valid = True
    else:
        pred_dir = pred_dag.astype(int)
        pred_undir = np.zeros_like(pred_dir, dtype=int)

    extra: Dict[str, Any] = {
        "threshold": float(threshold),
        "standardized_input": bool(standardize),
        "csnp_num_perm_samples": int(num_perm_samples),
        "pred_is_dag": pred_is_dag,
        "pred_cpdag_valid": pred_cpdag_valid,
        "pred_mec_size": mec_size,
    }
    extra.update(flat_directed_columns("csnp_prob", mean_prob))
    extra.update(flat_directed_columns("csnp_threshold_dag", pred_dag))
    return pred_dir, pred_undir, extra


