def find_h5_path(benchmark_root: Path, graph_id: int, num_samples: int, seed: int, num_datasets: int, mechanism: str, noise: str) -> Path:
    pattern = (
        benchmark_root
        / "3"
        / f"graph_{graph_id:02d}__*"
        / mechanism
        / noise
        / f"n_{num_samples}"
        / f"seed_{seed}"
        / f"benchmark_3var_numdatasets_{num_datasets}.h5"
    )
    matches = sorted(glob.glob(str(pattern)))
    if len(matches) != 1:
        raise FileNotFoundError(
            f"Expected exactly one h5 file for graph_id={graph_id}, n={num_samples}, "
            f"but found {len(matches)}. Pattern: {pattern}"
        )
    return Path(matches[0])


def attr_value(attrs: Dict[str, Any], key: str, default: Any) -> Any:
    v = attrs.get(key, default)
    if isinstance(v, bytes):
        return v.decode("utf-8")
    if isinstance(v, np.generic):
        return v.item()
    return v


def read_h5_metadata(h5_path: Path) -> Dict[str, Any]:
    with h5py.File(h5_path, "r") as f:
        attrs = dict(f.attrs)
        data_shape = tuple(f["data"].shape)
        label_shape = tuple(f["label"].shape)
    return {
        "graph_id": int(attr_value(attrs, "graph_id", -1)),
        "graph_name": str(attr_value(attrs, "graph_name", "unknown")),
        "graph_bitstring": str(attr_value(attrs, "graph_bitstring", "")),
        "num_samples": int(attr_value(attrs, "num_samples", data_shape[1])),
        "num_datasets": int(attr_value(attrs, "num_datasets", data_shape[0])),
        "data_shape": data_shape,
        "label_shape": label_shape,
    }


